#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;
	size_t	i;

	i = 0;
	s = (char*)malloc(size + 1);
	while (i <= size + 1)
	{
		s[i] = '\0';
		i++;
	}
	return (s);
}
